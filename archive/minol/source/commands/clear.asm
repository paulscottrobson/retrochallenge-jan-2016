;
;												CLEAR Command
;	
; ****************************************************************************************************************
; ****************************************************************************************************************

CMD_CLEAR:
	lpi 	p3,Variables 										; clear all variables
	ldi 	26
	st 		-1(p2) 												; set counter
__CCL_Loop:
	ldi 	0x00	 											; clear a variable
	st 		@1(p3)
	dld 	-1(p2) 												; decrement counter
	jnz 	__CCL_Loop											; do 26 times
	scl 														; set carry flag as ok.
	jmp 	CMD_CLEAR-2
