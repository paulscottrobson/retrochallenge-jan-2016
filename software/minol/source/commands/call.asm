;
;													CALL Command
;	
; ****************************************************************************************************************
; ****************************************************************************************************************

CMD_CALL:
	lpi 	p3,EvaluateHL-1 									; evaluate the H,L address
	xppc 	p3
	jp 		CMD_CALL-2 											; if CY/L = 0 then exit
	ld 		-2(p2) 												; copy address into P3
	xpal 	p3
	ld 		-1(p2)
	xpah 	p3
	ld 		@-1(p3) 											; fix up for pre increment
	xppc 	p3 													; call it
	scl 														; set carry link flag as okay
	jmp 	CMD_CALL-2